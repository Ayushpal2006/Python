a = "ayush"


print(len(a)) #len
print(a.capitalize()) #capitalize
print(a.find("s")) #find
print(a.count('h')) #count 
print(a.endswith('sh'))  #endsWith
print(a.replace('s','h'))  #replace

print(a.isalpha()) #is alphabet
print(a.isalnum()) #is alpha-numric
print(a.isdigit()) #is Digit
print(a.isspace()) #is space
print(a.isupper()) #is Upper
print(a.islower()) #is Lower
print(a.istitle()) #is Title
print(a.upper())  #converted to Upper 
print(a.lower())  #converted to LOwer
print(a.title())  #converted to Tittle
